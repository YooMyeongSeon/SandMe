package com.green.sandme.board.qna.vo;

public class QnaVO {
	private int qnaNum;
	private String qnaType;
	private String qnaEmail;
	private String qnaTel;
	private String qnaTitle;
	private String qnaContent;
	private String qnaAnswer;
	private String qnaDate;
	
	public int getQnaNum() {
		return qnaNum;
	}
	public void setQnaNum(int qnaNum) {
		this.qnaNum = qnaNum;
	}
	public String getQnaType() {
		return qnaType;
	}
	public void setQnaType(String qnaType) {
		this.qnaType = qnaType;
	}
	public String getQnaEmail() {
		return qnaEmail;
	}
	public void setQnaEmail(String qnaEmail) {
		this.qnaEmail = qnaEmail;
	}
	public String getQnaTel() {
		return qnaTel;
	}
	public void setQnaTel(String qnaTel) {
		this.qnaTel = qnaTel;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQnaContent() {
		return qnaContent;
	}
	public void setQnaContent(String qnaContent) {
		this.qnaContent = qnaContent;
	}
	public String getQnaAnswer() {
		return qnaAnswer;
	}
	public void setQnaAnswer(String qnaAnswer) {
		this.qnaAnswer = qnaAnswer;
	}
	public String getQnaDate() {
		return qnaDate;
	}
	public void setQnaDate(String qnaDate) {
		this.qnaDate = qnaDate;
	}

	
}
