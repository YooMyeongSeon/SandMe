package com.green.sandme.board.notice.vo;

public class NoticeVO {
	private int noticeNum;
	private String noticeUser;
	private String noticeTitle;
	private String noticeContent;
	private int noticeReadNum;
	private String noticeDate;

	
	public int getNoticeNum() {
		return noticeNum;
	}
	public void setNoticeNum(int noticeNum) {
		this.noticeNum = noticeNum;
	}
	public String getNoticeUser() {
		return noticeUser;
	}
	public void setNoticeUser(String noticeUser) {
		this.noticeUser = noticeUser;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public String getNoticeDate() {
		return noticeDate;
	}
	public void setNoticeDate(String noticeDate) {
		this.noticeDate = noticeDate;
	}
	public int getNoticeReadNum() {
		return noticeReadNum;
	}
	public void setNoticeReadNum(int noticeReadNum) {
		this.noticeReadNum = noticeReadNum;
	}
	
}
